/** 全出口管理データを取得 */
export function getAllExits(): ExitListItem[] {
  return mockExits;
}

/** ステータスでフィルタ */
export function getExitsByStatus(status: ExitStatus): ExitListItem[] {
  return mockExits.filter((e) => e.status === status);
}

/** IDで取得 */
export function getExitById(id: string): ExitListItem | undefined {
  return mockExits.find((e) => e.id === id);
}

/** 集計データ取得 */
export function getExitsTotals() {
  const exits = getAllExits();
  return {
    count: exits.length,
    confirmedCount: exits.filter((e) => e.status === "confirmed").length,
    negotiatingCount: exits.filter((e) => e.status === "negotiating").length,
    waitingCount: exits.filter((e) => e.status === "waiting_purchase").length,
    notPurchasedCount: exits.filter((e) => e.status === "not_purchased").length,
  };
}
